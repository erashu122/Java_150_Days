// Program to perform arithmetic operations on two numbers
class demo1 {

    public static void main(String[] args) {

        String n1="13";
        String n2="3";

        //convert string to integer
        int a=Integer.parseInt(n1);
        int b=Integer.parseInt(n2);

        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));   
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
        System.out.println("a%b="+(a%b));
    }

 

}
