public class demo4 {
  public static void main(String[] args) {
    // Logical operators
    boolean a = true;
    boolean b = false;
    System.out.println("a && b: " + (a && b));
    System.out.println("a || b: " + (a || b));
    System.out.println("!(a && b): " + !(a && b));
  }
    
}
